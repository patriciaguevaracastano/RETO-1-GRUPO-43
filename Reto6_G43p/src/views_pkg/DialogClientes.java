    void show_pet_owners() {
        String sql = "SELECT * FROM `tb_pet_owners` WHERE 1";
        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            //Los datos que devuelve la consulta se muestran en la tabla
            Object[] pet_owner = new Object[6];
            modelo = (DefaultTableModel) tb_pet_owners.getModel();
            while (rs.next()) {
                pet_owner[0] = rs.getInt("id");
                pet_owner[1] = rs.getString("owner");
                pet_owner[2] = rs.getString("document_type");
                pet_owner[3] = rs.getInt("document");
                pet_owner[4] = rs.getString("contact");
                pet_owner[5] = rs.getString("gender");
                modelo.addRow(pet_owner);
                System.out.println(rs.getInt("id"));

            }
            tb_pet_owners.setModel(modelo);
        } catch (SQLException e) {

        }
    }
